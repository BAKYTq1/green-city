    let lenis;
    let rafId;

    (async () => {
      const { default: Lenis } = await import('@studio-freight/lenis');
      lenis = new Lenis();

      const raf = (time) => {
        lenis.raf(time);
        rafId = requestAnimationFrame(raf);
      };
      rafId = requestAnimationFrame(raf);
    })();

