const ERROR_MESSAGES = {
  400: 'Некорректный запрос. Проверьте введённые данные.',
  401: 'Сессия истекла. Пожалуйста, войдите снова.',
  403: 'Недостаточно прав для этого действия.',
  404: 'Данные не найдены.',
  408: 'Сервер не ответил вовремя. Попробуйте ещё раз.',
  422: 'Проверьте правильность заполнения формы.',
  429: 'Слишком много запросов. Подождите немного.',
  500: 'Что-то пошло не так на сервере. Попробуйте позже.',
  502: 'Сервер временно недоступен.',
  503: 'Сервис на техническом обслуживании. Попробуйте позже.',
}

export function parseError(error) {
  if (!navigator.onLine) {
    return 'Нет соединения с интернетом.'
  }

  if (error?.message?.toLowerCase().includes('fetch')) {
    return 'Не удалось подключиться к серверу. Попробуйте позже.'
  }

  const status = error?.status || error?.response?.status
  if (status && ERROR_MESSAGES[status]) {
    return ERROR_MESSAGES[status]
  }

  return 'Произошла ошибка. Попробуйте ещё раз.'
}